char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*s2;

	s2 = dest;
	while (*src != '\0' && n > 0)
	{
		*dest++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*dest++ = '\0';
		n--;
	}
	return (s2);
}
